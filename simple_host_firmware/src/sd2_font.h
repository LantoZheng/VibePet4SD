inline void drawChar5x7(int x, int y, char c, uint16_t fg, uint16_t bg, int scale = 3) {
  if (c < 32 || c > 90) c = 32;
  const uint8_t* bmp = FONT5X7[c - 32];
  for (int col = 0; col < 5; col++) {
    uint8_t line = bmp[col];
    for (int row = 0; row < 7; row++) {
      uint16_t color = (line & (1 << row)) ? fg : bg;
      tft.fillRect(x + col * scale, y + row * scale, scale, scale, color);
    }
  }
}

inline void drawText5x7(int x, int y, const String& s, uint16_t fg, uint16_t bg, int scale = 3) {
  int cx = x;
  for (size_t i = 0; i < s.length(); i++) {
    if (s[i] == '\n') { cx = x; y += 8 * scale; continue; }
    drawChar5x7(cx, y, s[i], fg, bg, scale);
    cx += 6 * scale;
  }
}

inline void drawTitle(const String& title, uint16_t color) {
  tft.fillScreen(TFT_BLACK);
  int bw = 24; // Signal::BORDER_W, hardcoded to avoid circular dep
  tft.fillRect(bw, bw, SCREEN_W - 2*bw, 22, color);
  int tw = title.length() * 12;
  drawText5x7((SCREEN_W - tw) / 2, bw + 3, title, TFT_BLACK, color, 2);
}

inline void drawTextMessage(const String& message) {
  extern String currentScreen;
  currentScreen = "text";
  drawTitle("HOST TEXT", TFT_MAGENTA);
  String msg = message.substring(0, 36);
  drawText5x7((SCREEN_W - msg.length()*12)/2, 118, msg, TFT_WHITE, TFT_BLACK, 2);
}
